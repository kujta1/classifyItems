from groq import Groq

client = Groq()

formattedData = """
Стаклокерамичка плотна со брусен раб, 4 Hilight грејни места,
TouchControl, Stop&Go, одржување на топлина, заштита од деца.
Димензии 595x54x520 мм.
Рерна: 77L, енергетска класа A, чистење со AquaClean,
димензии 595x595x564 мм.
"""

prompt = f"""
Ти си експерт за извлекување и структурирање на податоци за производи.

ЦЕЛ:
Од дадениот опис на производ (на македонски јазик), извлечи структурирани податоци и карактеристики.

СТРОГИ ПРАВИЛА:
- Излезот МОРА да биде валиден JSON.
- СИТЕ клучеви и вредности МОРА да бидат на македонски јазик.
- Не измислувај податоци.
- Ако недостасува вредност, постави null.

JSON СТРУКТУРА:
(користи ја структурата од описот погоре)

ВЛЕЗ:
{formattedData}
"""

response = client.chat.completions.create(
    model="llama-3.3-70b-versatile",
    messages=[{"role": "user", "content": prompt}],
    temperature=0
)

print(response.choices[0].message.content)
